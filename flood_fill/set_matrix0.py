class Solution():
    def set_matrix_zeros(self, matrix):

        visited = [[False for r in range(len(matrix[0]))] for c in range(len(matrix))]
        
        def fill_row(y):
            for i in range(len(matrix)):
                if i == y:
                    for j in range(len(matrix[y])):
                        if matrix[y][j] != 0:
                            matrix[y][j] = 0
                            visited[y][j] = True

        def fill_col(x):
            for i in range(len(matrix)):
                
                if matrix[i][x] != 0:
                    matrix[i][x] = 0
                    visited[i][x] = True

        def set_matrix_zeros(x, y):
            if y < 0 or x < 0 or y >= len(matrix) or x >= len(matrix[0]):
                return
            
            if visited[y][x]:
                return

            fill_row(y)
            fill_col(x)
        
        for y in range(len(matrix)):
            for x in range(len(matrix[0])):
                if matrix[y][x] == 0:
                    set_matrix_zeros(x, y)



